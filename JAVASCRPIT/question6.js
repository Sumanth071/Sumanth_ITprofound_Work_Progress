let count = 1;
console.log(count);
console.log(++count); // Pre-increment: increments first, then returns the value
console.log(count++); // Post-increment: returns the value first, then increments
console.log(count);
//explaination:
// 1  → initial count
// 2  → pre-increment (++count)
// 2  → post-increment (count++)
// 3  → final value of count